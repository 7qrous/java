
public class LiteralEx {
    public static void main(String[] args) {
//      리터럴 : 소스코드 내에 존재하고 사용하는 데이터 자체
//      정수, 실수, 문자, 문자열, 논리 리터럴 종류가 있음
//      문자리터럴 : 문자 1개
//      문자열리터럴 : 문자가 여러개 모아져 있는 것, 여러개의 문자 집합
//      논리 리터럴 : 데이터가 참, 거짓인지 나타냄

        // 10진수
        int var1 = 10; // 10은 정수 리터럴 : 데이터 그 자체 , var1 변수
        System.out.println(var1);

        // 8진수
        int var2 = 010; // 8진수 리터럴을 var2 변수
        System.out.println(var2);

        // 16진수
        int var3 = 0x10; // 16진수 리터럴을 var3 변수
        System.out.println(var3);


    }
}
